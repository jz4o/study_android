package jp.co.jz4o.studyandroid.ObjectListView;

public class Student {
    private int number;
    private String name;

    public Student(final int number, final String name) {
        this.number = number;
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

    public String getName() {
        return name;
    }
}
